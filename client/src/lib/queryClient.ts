/**
 * TanStack Query client configuration — provides default fetcher, error handling, and caching strategy for all API requests.
 */
import { QueryClient, QueryFunction } from "@tanstack/react-query";

/** Throws an error with status code and response text for non-2xx responses. Used by both apiRequest and the default query function. */
async function throwIfResNotOk(res: Response) {
  if (!res.ok) {
    const text = (await res.text()) || res.statusText;
    throw new Error(`${res.status}: ${text}`);
  }
}

/** General-purpose API request helper for mutations (POST, PATCH, DELETE). Includes credentials for session cookies. Used with useMutation hooks. */
export async function apiRequest(
  method: string,
  url: string,
  data?: unknown | undefined,
): Promise<Response> {
  const res = await fetch(url, {
    method,
    headers: data ? { "Content-Type": "application/json" } : {},
    body: data ? JSON.stringify(data) : undefined,
    credentials: "include",
  });

  await throwIfResNotOk(res);
  return res;
}

/** Default query function factory for useQuery hooks. Constructs the URL from the queryKey array. Supports configurable 401 handling (throw or return null). */
type UnauthorizedBehavior = "returnNull" | "throw";
export const getQueryFn: <T>(options: {
  on401: UnauthorizedBehavior;
}) => QueryFunction<T> =
  ({ on401: unauthorizedBehavior }) =>
  async ({ queryKey }) => {
    const res = await fetch(queryKey.join("/") as string, {
      credentials: "include",
    });

    if (unauthorizedBehavior === "returnNull" && res.status === 401) {
      return null;
    }

    await throwIfResNotOk(res);
    return await res.json();
  };

/** Global QueryClient with aggressive caching (staleTime: Infinity, no refetch on focus). Data is only refreshed when explicitly invalidated after mutations. */
export const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      queryFn: getQueryFn({ on401: "throw" }),
      refetchInterval: false,
      refetchOnWindowFocus: false,
      staleTime: Infinity,
      retry: false,
    },
    mutations: {
      retry: false,
    },
  },
});
